<h3>What is RSS Feed and how to use it?</h3>

<p>
RSS (Rich Site Summary) is a format for delivering regularly changing web content. Many news-related sites, weblogs and other online publishers 
syndicate their content as an RSS Feed to whoever wants it. RSS solves a problem for people who regularly use the web. It allows you to easily 
stay informed by retrieving the latest content from the sites you are interested in. You save time by not needing to visit each site individually. 
You ensure your privacy, by not needing to join each site’s email newsletter. Feed Reader or News Aggregator software allow you to grab the RSS 
feeds from various sites and display them for you to read and use. A variety of RSS Readers are available for different platforms. Some popular 
feed readers include Amphetadesk (Windows, Linux, Mac), FeedReader (Windows), and NewsGator (Windows – integrates with Outlook). There are also a 
number of web-based feed readers available. My Yahoo , Bloglines , and Google Reader are popular web-based feed readers. Once you have your Feed 
Reader, it is a matter of finding sites that syndicate content and adding their RSS feed to the list of feeds your Feed Reader checks. Many sites 
display a small icon with the acronyms RSS, XML, or RDF to let you know a feed is available. Once you pick an RSS feed reader, it is time to 
subscribe to RSS feeds.
</p>

<p>
Popular RSS Readers include Amphetadesk, FeedReader, NewsGator, and web-based readers 
like My Yahoo and others.
</p>
<br>

<h3>How to Subscribe to RSS Feed?</h3>

<ul>
<li>Follow the instructions of your RSS feed reader. Each RSS feed reader is a little different.</li>
<li>On a website or blog with a feed, find the RSS icon or button, left click, and see if the RSS Feed has information to help subscribe.</li>
<li>On a website or blog with a feed, find the RSS icon or button, right click, copy link location, and paste the URL into your RSS feed reader.</li>
</ul>
