<div class="content">

<p><strong>Namaskar!</strong></p>

<p>I consider it an honour and a great responsibility to be in charge of the historic and prestigious National Centre for Disease Control (NCDC) 
    India, as its 26 th  Director. Six months have elapsed since charge was assumed, and it is time to re-look at the spectrum of national and 
    international public health work being done by this organization and the path needed to be adapted now, and in future.</p>

<p>I also take this opportunity to assure that the good work initiated by my predecessors is being taken forward even more vigorously with various 
    newer initiatives as well. Simultaneously, I extend my heartiest greetings and thanks to NCDC’s multi-disciplinary public-health workforce for 
    their remarkable knowledge, skills, dedication and determined efforts to make NCDC of global significance.</p>

<p>I acknowledge with pride that NCDC, throughout its 115  glorious years, has discharged its mandate of disease surveillance, outbreak 
    investigation, rapid containment and research very effectively. It has deftly handled SARS, Avian Influenza, Novel H1N1 Influenza, Zika, 
    Nipah, COVID 19, Chandipura disease, Scrub typhus, Leptospirosis, etc. and the more recent M-pox, Guillian Barre syndrome, Avian-influenza, 
    etc. Our pride moves a notch higher as the global acclaim received by NICD/ NCDC is noted w.r.t. eradication/ elimination of Small Pox, Guniea 
    Worm disease, Yaws and Poliomyelitis.</p>

<p>It’s also in context here to acknowledge that, this grand old organization has transformed constantly, beginning as Central Malaria Bureau 
    (1909, Shimla), progressing through Malaria Institute (1938, Delhi), National Institute of Communicable Diseases (NICD 1963, Delhi) and 
    finally as National CDC (2009) in response to changing demographic, disease, epidemiological, and environmentalprofiles. The modern NCDC now, 
    therefore, has a six-pronged mandate: Modernized Surveillance, Strengthened Laboratory Networks, well prepared Public Health Workforce, 
    Enhanced Pandemic Preparedness & Emergency Response, National focal point (NFP) for International Health Regulations (IHR) including 
    chemical toxicants and Non-communicable Diseases.</p>

<p>It gives me immense satisfaction to share that work is progressing well to strengthen and expand NCDC’s mandate, through the Government of 
    India’s flagship programme, the Prime Minister’s Aayushman Bharat Health Infrastructure Mission (PM-ABHIM) which has six main components: 
    5 Regional Centres, 27 State/ UT Branches, 20 Metropolitan Surveillance Units, 10 Bio-Safety Level 3, Strengthening IDSP & IHIP, and 
    Strengthening and upgradation of NCDC Headquarters at New Delhi.</p>

<p>Simultaneously, steps are also underway to synergize, harmonize and upscale the functioning and implementation of national health programs 
    of the Ministry run from NCDC. Prominent among those are the Integrated Disease Surveillance Program (IDSP), the National Programme for 
    Climate Change for Human Health, the National One Health Program for Control of Zoonotic Diseases, the Viral Hepatitis Control & Surveillance 
    Program, the Rabies Control Program, the Leptospirosis Control Program, the Snakebite Envenomation Program, the Anti Microbial Resistance 
    Containment Program, etc. Similarly, NCDs are also now included in the mandate of this organization, and technical support is being provided 
    to the national NCD Programme through capacity building and issuance of technical guidelines.</p>

<p>
    While all the National programmes run by NCDC on behalf of the MoHFW are doing commendable work, one needs special mention here – 
    the Integrated Disease Surveillance Programme (IDSP). The IDSP through the digital Integrated Health Information Platform (IHIP), 
    conducts near real-time, case-based monitoring of 52 epidemic-prone diseases/ health conditions now including the VPDs also, across the 
    length and breadth of this country of 1.47 billion. Rapid containment measure are initiated, jointly with States/ UTs, as soon as any 
    excess/ clustering is detected. This elaborate system includes the establishment and strengthening of the Block Public Health Units (BPHU), 
    District Public Health Labs and taking over of VPD surveillance from the WHO-NPSN network.
</p>  

<p>
    I also take this opportunity to appreciate all my predecessor’s efforts to leverage cutting-edge technology in diagnostics including molecular 
    genomics jointly with ICMR Institutions including the INSACOG and VRDL network, the National Polio, Measles/ Rubella Labs jointly with the 
    WHO-NPSN, and for bringing digital health technologies for near real-time disease surveillance. More new initiatives/ cutting-edge research 
    has been initiated at NCDC recently prominent of which are (i) ZooWin digital solution for streamlining the management of anti-rabies vaccine/ 
    serum and anti-snake venom with UNDP support, (ii) Surveillance of human-spillable zoonosis in bird-sanctuaries jointly with wildlife 
    diagnostic facilities and their being captured on IHIP portal under ICMR leadership/ guidance and (iii) Genome sequencing of mosquito species 
    coming aboard ships from foreign countries, also under joint ICMR and PH(IH) leadership and guidance.
</p>

<p>
    Above accomplishments have been possible only because of unwavering support, cooperation and guidance of the Directorate General of Health 
    Services (DGHS) and the Ministry of Health & Family Welfare, Govt. of India, and the strategic partnership with International agencies 
    (e.g. WHO, UNDP, World Bank, ADB, the US CDC, etc.), Regional groupings (viz. ASEAN, SAARC, QUAD, Indo-Pacific Group of nations, BRICS, 
    G20, etc.), National Institutions (e.g. National Institute for Transforming India – NITI Aayog, Indian Council of Medical Research – ICMR, 
    National Disaster Management Authority – NDMA, National Institute of Disaster Management – NIDM, National Institute of One Health – NIOH, 
    All India Institute of Hygiene & Public Health – AIIH&PH), State Health Authorities and Institutions, and Universities within and outside 
    the country. NCDC remains indebted to all these institutions and looks forward to continued partnership, support and guidance as its 
    march ahead and envisages a greater role in national and international public health arena.
In order to enhance the service delivery and provide better hand holding for States/ UTs, a three-pronged strategy is now being followed by the 
programmes and divisions: (i) close coordination and synergy of actions with the Regional Offices of H&FW (ROHFW), the Points of Entry (POE), 
the National Health Mission (NHM), and other National Health Programmes viz. NVBDCP, RCH/ Immunization Division, and (ii) boosting of operations 
and implementation research across programmes, and (iii) Fast-tracking digital innovations under guidance of the Digital Health Mission.
</p>

<p>It is also my privilege to share here that the stage is now set for NCDC to take the next leap in effectively managing the frequently occurring 
    emerging/ re-emerging and zoonotic disease outbreaks, the inter-species spill-overs while also aiming to forecast/ predict them with precision.
    For this NCDC has started exploring newer, affordable and scalable technologies avaible with other Ministries/ Departments viz. 
    Animal Husbandry (DAHD), Forests Environment & Climate Change (MOEFCC), India Meteorological Department (IMD), Department of Biotechnology 
    (DBT), the Council for Scientific & Industrial Research (CSIR), the Indian Institutes of Technology (IITs), the Indian Institute of Science 
    (IISc, Bangalore), and the Indian Space Research Organization (ISRO), etc. under the guidance of the MOHFW, the ICMR, the Principal Scientific 
    Advisor’s (PSA) Office to the Government of India, and the NITI Aayog.</p>

<p>
    Additionally, to strengthen the public health network within and outside the country, and to turn this organization into a vibrant, 
    world-class national public health institution, NCDC might consider forming a Consortia with similarly placed Institutions viz. All 
    India Institute of Hygiene & Public Health (AIIH&PH), National Institute of Health & Family Welfare (NIHFW), and National Institute of 
    Public Health Training & Research (NIPHTR), in first instance. Subsequently, other institutions like the National Centre for Vector Borne 
    Diseases Control (NCVBDC), National Health Systems Resource Centre (NHSRC), NITI, etc. could also be included.
</p>

<p>It is my strong conviction that above proposed strategies, if implemented, has the potential to turn NCDC into a vibrant and truly 
    global public health institute.</p>

