<h2 class="organsiation-title">Organisations Structure</h2>

  <p class="organisation-text">
    The NCDC is under the administrative control of the Directorate General of Health Services, Ministry of Health and Family Welfare, 
    Government of India. The Director, an officer of the Public Health sub-cadre of Central Health Service, is the administrative and 
    technical head of the Institute and is also Programme Director of Integrated Disease Surveillance Programme (IDSP), National Anti-Microbial 
    Resistance (AMR) Containment Programme, National Rabies Control Programme (NRCP), Programme for Prevention and Control of Leptospirosis (PPCL),
    Inter-sectoral Coordination Programme for Prevention and Control of Zoonotic Diseases (ISCPCZD) and National Programme on Surveillance of 
    Viral Hepatitis (NPSVH) in different geographical locations.
<br><br>
The NCDC has its headquarters in Delhi and has eight outstation branches located at Alwar (Rajasthan), Bengaluru (Karnataka), 
Kozhikode-Calicut (Kerala), Coonoor (Tamil Nadu), Jagdalpur (Chhatisargh), Patna (Bihar), Rajahmundry (Andhra Pradesh) and 
Varanasi (Uttar Pradesh). There are 14 Technical Centres/Divisions at the headquarters viz. Centre of Epidemiology, Division of 
Microbiology, Division of Zoonosis, Centre for Medical Entomology & Vector Management (CME&VM), Integrated Disease Surveillance Programme (IDSP), 
Centre for AIDS & Related Diseases (CARD), Division of Biochemistry & Toxicology, Division of Biotechnology, Division of Parasitic Disease (DPD),
Division of Malariology & Coordination (M&C), Centre for Environment & Occupational Health (CE&OH), Centre for Non-Communicable Diseases (CNCD), 
Statistical Monitoring & Evaluation Centre (SM&EC) and Division of Planning, Budget & Administration (PBA). In each centre/division, 
there are sections and laboratories dealing with different communicable and non-communicable diseases. The activities of each division are 
supervised by the officer-in charge supported by medical and non-medical scientists, research officers and other technical and paramedical staff. The NCDC branches are also equipped and staffed to carry out field studies, training activities and research. The NCDC at its headquarter has 58 Group A officers (Central Health Services and Non-Medical Scientists), 10 Group B (Gazetted), 111 Group B (Non-Gazetted) and 137 Group C officials besides about 37 contractual consultants/support staff. The existing eight NCDC branches have 81 officials. 
The Budget allocated for NCDC is Rs. 233.04 crores (approx. USD 35 million) for financial year 2017-18 including for upgradation of NCDC.
  </p>

